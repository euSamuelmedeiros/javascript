var agora = new Date();
var diaSem = agora.getDay();

/* 0 - domingo
   1 - segunda
   2 - terça
   3 - quarta
   4 - quinta
   5 - sexta
   6 - sabado
*/

switch(diaSem) {
    case 0:
        console.log('domingo')
        break
}
switch(diaSem) {
    case 1:
         console.log('segunda')
         break
}
switch(diaSem) {
    case 2:
        console.log('terça')
        break
}
switch(diaSem) {
    case 3:
        console.log('quarta')
        break
}
switch(diaSem) {
    case 4: 
    console.log('quinta')
    break
}
switch(diaSem) {
    case 5:
        console.log('sexta')
        break
}
switch(diaSem) {
    case 6:
        console.log('sabado')
        break
}